function getPlanetName(id){
  var name;
  switch(id){
    case 1:
      name = 'Yogi'
      break;
    case 2:
      name = 'Basu'
      break;
    case 3:
      name = 'Mahi'
      break;
    case 4:
      name = 'Mallu'
      break;
    case 5:
      name = 'Manju'
      break;
    case 6:
      name = 'Appu'
      break;
    case 7:
      name = 'Raju'
      break;
    case 8:
      name = 'Sanju'
      break;
  }
  return name;
}